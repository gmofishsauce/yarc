	log.SetFlags(log.Lmsgprefix | log.Lmicroseconds)
	log.SetPrefix("clx: ")
	
	f, err := os.Open(src)
	if err != nil {
		log.Fatal(err)
	}
	defer f.Close()
	pn, err := ParseReader("", f)
