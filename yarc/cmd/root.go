package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "yarc",
	Short: "Host software for Yet Another Retro Computer - YARC",
	Long: `The yarc command collects all host software functionality for YARC.
It includes the YARC serial line monitor ("yarc host"), the
assembler and microassembler ("yarc asm"), and some other commands.`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

